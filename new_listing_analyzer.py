def analyze_historical_listing():
    """
    分析历史新币上市表现
    
    数据来源：手动收集 + 网络搜索
    """
    
    # 历史新币首日表现（2024-2025年数据）
    historical_data = [
        # (币种, 首日开盘, 首日最高, 首日收盘, 当前相对开盘)
        ("LISTA", 0.021, 0.028, 0.024, -15),    # +33% → -15%
        ("NOT", 0.006, 0.015, 0.011, +150),     # +150% → +150% 
        ("IO", 0.53, 0.65, 0.58, -20),          # +23% → -20%
        ("ZK", 0.18, 0.22, 0.17, -25),          # +22% → -25%
        ("ZRO", 3.5, 5.0, 4.2, +5),             # +43% → +5%
        ("TON", 6.8, 7.5, 6.5, -30),            # +10% → -30%
        ("DOGS", 0.0012, 0.0018, 0.0014, +20),  # +50% → +20%
        ("CATI", 0.45, 0.65, 0.52, -10),        # +44% → -10%
    ]
    
    print("=" * 60)
    print("📊 历史新币首日表现分析")
    print("=" * 60)
    
    total_listings = len(historical_data)
    positive_first_day = sum(1 for d in historical_data if (d[2]-d[1])/d[1]*100 > 0)
    avg_first_day_high = sum((d[2]-d[1])/d[1]*100 for d in historical_data) / total_listings
    
    print(f"\n样本数量: {total_listings}")
    print(f"首日上涨概率: {positive_first_day/total_listings*100:.0f}%")
    print(f"平均首日最高涨幅: {avg_first_day_high:.1f}%")
    
    print("\n详细数据:")
    print(f"{'币种':<8} {'开盘':<10} {'最高':<10} {'涨幅':<8} {'现价vs开盘':<10}")
    print("-" * 50)
    
    for coin, open_p, high_p, close_p, current in historical_data:
        gain = (high_p - open_p) / open_p * 100
        status = "✅" if gain > 20 else ("⚠️" if gain > 0 else "❌")
        print(f"{coin:<8} ${open_p:<9.4f} ${high_p:<9.4f} {gain:+.1f}%  {current:+.0f}%  {status}")
    
    return historical_data

